    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double Lado, DiagonalMayor, DiagonalMenor, Area, Perimetro;

        Lado = Double.parseDouble(txtLado.getText());
        DiagonalMayor = Double.parseDouble(txtDiagonalMayor.getText());
        DiagonalMenor = Double.parseDouble(txtDiagonalMenor.getText());
        
        Rombo figura5 = new Rombo(Lado, DiagonalMayor, DiagonalMenor);
        Area = figura5.calcularArea();
        Perimetro = figura5.calcularPerimetro();
        
        txtArea.setText(String.valueOf(Area));
        txtPerimetro.setText(String.valueOf(Perimetro));
    }//GEN-LAST:event_btnCalcularActionPerformed

